#A Python program demonstrating calculation of mean, variance, and standard deviation using NumPy.
import numpy as np

arry=np.array([2,3,4,5,6])

mean=np.mean(arry)

varinc=np.var(arry)

stddeviation=np.std(arry)

print("mean is:", mean)

print("Vartance is:", varinc) # How far the values are dispersed from the Mean

print("Standard Deviation is", stddeviation)

# Sandard Deviation is The Amount of dispersion of Given. Data Set Values